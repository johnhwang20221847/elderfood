function totalScore(answers){
  return QUESTIONS.reduce((s,q)=> s + (answers[q.id]||0) * DOMAINS[q.dom].weight, 0);
}
function domainScore(dom, answers){
  return QUESTIONS.filter(q=>q.dom===dom).reduce((s,q)=> s + (answers[q.id]||0)*DOMAINS[dom].weight, 0);
}
function stageFor(total){
  if(total>=80) return 1; if(total>=55) return 2; if(total>=30) return 3; return 4;
}

